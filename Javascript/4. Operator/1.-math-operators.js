// Operator Aritmatika JavaScript
let a = 10;
let b = 2;

// +  : Penjumlahan
console.log(a + b);

// -  : Pengurangan
console.log(a - b);

// *  : Perkalian
console.log(a * b);

// ** : Eksponen (pangkat), tersedia sejak ES2016
console.log(a ** b);

// /  : Pembagian
console.log(a / b);

// %  : Modulus (sisa bagi)
console.log(a % b);

// ++ : Increment (menambah 1)
console.log(a++);

// -- : Decrement (mengurangi 1)
console.log(a--);
